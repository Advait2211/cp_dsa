import math
def solve():
    n = int(input())

    # pizza - 9 - 3 3 3
    # 16 - 5 5 6, 1 1 4, 1 1 2 = 7
    # 15 - 5 5 5, 1 1 3, 1 1 1 = 7
    # 14 - 4 4 6, 2 2 2 = 6
    # 13 - 4 4 5, 1 1 3, 1 1 1 = 6
    # 12 - 4 4 4, 1 1 2 = 5
    # 11 - 3 3 5, 1 1 3, 1 1 1 = 5
    # 10 - 3 3 4, 1 1 2 = 4
    # 9 - 3 3 3, 1 1 1 = 4
    # 8 - 2 3 3, 1 2 0 = 3
    # 7 - 2 2 3, 1 1 1 = 3
    # 6 - 2 2 2 = 2
    # 5 - 1 1 3, 1 1 1 = 2
    # 4 - 1 1 2 = 1


    # 15 - 7, 6 - 2
    # 15 - 6 = 9 / 2 = 4.5(ceil) = 5

    # 11 - 6 = 5 / 2 = 3
    # 12 - 6 = 6 / 2 = 3

    if n <= 6:
        if n == 3 or n == 4:
            return 1
        else:
            return 2
    else:
        res = math.ceil((n - 6) / 2)
        return 2 + res



t = int(input())
for _ in range(t):
    print(solve())